# Program to provide the condition statement according to the given number
# Asking user to provide input data

x = int(input("Enter the integer: "))

# Checking condition and print statement
if x == 10 or x == 20:
    print("number is 10 or 20")
elif x == 100 or x == 200:
    print("number is 100 or 200" )
else:
    print(f"number is {x}")

